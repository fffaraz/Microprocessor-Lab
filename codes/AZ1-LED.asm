	.INCLUDE "M32DEF.INC"
	.ORG 0X0000
	JMP MAIN
	.ORG 0X0050

MAIN:
	LDI R16,LOW(RAMEND)
	OUT SPL,R16
	LDI R16,HIGH(RAMEND)
	OUT SPH,R16

	LDI R16,0XFF
	OUT DDRC,R16

	LDI R16,0X01

LOOP:
	OUT PORTC,R16
	CALL DELAY
	LSL R16
	BRNE LOOP
	LDI R16,0X01
	JMP LOOP

DELAY:
	LDI R18,255
DELAY_BODY:
	LDI R19,255
DELAY_BODY2:
    LDI R20,4
DELAY_BODY3:
    DEC R20  
	BRNE DELAY_BODY3
	DEC R19
	BRNE DELAY_BODY2
	DEC R18
	BRNE DELAY_BODY
	RET
